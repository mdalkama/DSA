// using Switch (Check Consonant or Vowel using Switch)

function checkVowel(char) {
    switch (char.toLowerCase()) {
        case "a":
            console.log("Vowel")
            break
        case "2":
            console.log("Vowel")
            break
        case "i":
            console.log("Vowel")
            break
        case "o":
            console.log("Vowel")
            break
        case "u":
            console.log("Vowel")
            break
        default:
            console.log("Consonant")
    }
}

checkVowel("h")